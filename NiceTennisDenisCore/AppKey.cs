namespace NiceTennisDenisCore
{
    /// <summary>
    /// Enumeration of appsetting keys.
    /// </summary>
    internal enum AppKey
    {
        /// <summary>
        /// Connection string pattern.
        /// </summary>
        ConnectionStringPattern,
        /// <summary>
        /// SQL instance.
        /// </summary>
        SQL_instance,
        /// <summary>
        /// SQL database (ATP).
        /// </summary>
        SQL_db_atp,
        /// <summary>
        /// SQL database (WTA).
        /// </summary>
        SQL_db_wta,
        /// <summary>
        /// SQL user.
        /// </summary>
        SQL_uid,
        /// <summary>
        /// SQL password.
        /// </summary>
        SQL_pwd,
        /// <summary>
        /// Datas directory.
        /// </summary>
        DatasDirectory,
        /// <summary>
        /// Configuration identifier.
        /// </summary>
        ConfigurationId
    }
}
